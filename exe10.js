//Faça um script que, usando apenas um laço for, exiba as duas colunas a seguir:
//a primeira começa do 0 em forma crescente e a segunda começa do 10 decrementando.

let numero = 10;

// Usando um laço for para criar as duas colunas
for (let i = 0; i <= 10; i++) {
    console.log(`${i}  ${numero}`); // Exibe o valor de i e da segunda coluna
    numero--; // Decrementa a segunda coluna
}
